def train():
    parser = argparse.ArgumentParser('Evaluation with weighted k-NN on ImageNet')
    parser.add_argument('--num_workers', default=10, type=int, help='Number of data loading workers per GPU.')
    parser.add_argument('--data_path', default='/path/to/imagenet/', type=str)
    parser.add_argument('--dataset_name', default='tumorbank', type=str)
    parser.add_argument('--seed', default=0, type=int)
    parser.add_argument('--lr', default=0.02, type=float)
    parser.add_argument('--epoch', default=30, type=int)

    args = parser.parse_args()
    utils.fix_random_seeds(args.seed)

    for fold in range(3):
        train_dataset = MyDataset(args.data_path, args.dataset_name,
                                  get_folded_study_ids(None, 3, args.dataset_name)[fold][0])
        test_dataset = MyDataset(args.data_path, args.dataset_name,
                                 get_folded_study_ids(None, 3, args.dataset_name)[fold][1])
        train_loader = DataLoader(train_dataset, batch_size=256, shuffle=False, drop_last=False,
                                  num_workers=args.num_workers)
        test_loader = DataLoader(test_dataset, batch_size=256, shuffle=False, drop_last=False,
                                 num_workers=args.num_workers)

        model = torch.nn.Linear(384, 1).cuda()
        opt = torch.optim.Adam(model.parameters(), lr=args.lr)

        metric_logger = utils.MetricLogger(delimiter="  ")
        for epoch in metric_logger.log_every(range(args.epoch), 1):
            model.train()
            for feature, censor, time, _ in metric_logger.log_every(train_loader, 1):
                feature = feature.cuda()
                censor = censor.cuda()
                time = time.cuda()

                opt.zero_grad()
                pred = model(feature)
                loss = cox_loss(time, censor, pred)
                loss.backward()
                opt.step()

            metrics = test(model, test_loader)
            metric_logger.update(**metrics)

train()
