def create_folds(study_ids, k):
    folds = []
    for train_idx, test_idx in sklearn.model_selection.KFold(n_splits=k, shuffle=False).split(study_ids):
        folds.append((study_ids[train_idx], study_ids[test_idx]))
    return folds


def get_folded_study_ids(study_ids, k, dataset_name):
    dataset_name = dataset_name.lower()
    folds = None
    if dataset_name == 'tumorbank':
        folds = tb_folded_study_ids(k)
    if dataset_name == 'otta':
        folds = otta_folded_study_ids(k)
    if dataset_name == 'jhu':
        folds = jhu_folded_study_ids(k)
    if folds is None:
        folds = create_folds(study_ids, k)
    return folds


class MyDataset(Dataset):
    def __init__(self, root, dataset_name, ids):
        super(MyDataset, self).__init__()
        self._outcome = pd.read_csv(os.path.join(self.raw_dir, 'outcome.csv'))
        self._core_to_study_id = pd.read_csv(os.path.join(self.raw_dir, 'core_id.csv'))
        self._invalid_study_ids = pd.read_csv(os.path.join(self.raw_dir, 'invalid_study_ids.csv')).study_id.values
        self._dataset_name = dataset_name
        self.files = []
        for f in [os.path.join(root, f) for f in os.listdir(root) if f.endswith(".pt")]:
            data, valid = self._read_record(f, ids)
            if not valid:
                continue
            self.files.append(data)

    def _read_record(self, f: str, ids):
        feature = torch.load(f)
        core_id = filename_to_patient_id(f, self._dataset_name)
        study_id = self._core_to_study_id.loc[self._core_to_study_id.core_id == core_id]
        if study_id.shape[0] == 0:
            return None, False
        assert study_id.shape[0] == 1
        study_id = study_id.study_id.values[0]

        if study_id in self._invalid_study_ids:
            return None, False

        if study_id not in ids:
            return None, False

        patient_outcome = self._outcome.loc[self._outcome.study_id == study_id]
        if patient_outcome.shape[0] == 0:  # have to skip the patients without known status
            return None, False

        censored = patient_outcome.status.values[0] == 'censor'
        partial = patient_outcome.status.values[0] == 'partial'
        time = patient_outcome.time.values[0]

        if partial:
            return None, False

        return (feature, censored, time, study_id), True

    def __len__(self):
        return len(self.files)

    def __getitem__(self, item):
        return self.files[item]


SMALL_NUM = np.log(1e-45)


def cox_loss(survtime, censor, hazard_pred, decoupling=False):
    # This calculation credit to Travers Ching https://github.com/traversc/cox-nnet
    # Cox-nnet: An artificial neural network method for prognosis prediction of high-throughput omics data
    censor = censor.type(torch.FloatTensor).to(hazard_pred.device)
    current_batch_len = len(survtime)
    R_mat = np.zeros([current_batch_len, current_batch_len], dtype=int)
    for i in range(current_batch_len):
        for j in range(current_batch_len):
            R_mat[i, j] = survtime[j] >= survtime[i]

    R_mat = torch.FloatTensor(R_mat).to(hazard_pred.device)
    censor = censor.reshape(-1)
    theta = hazard_pred.reshape(-1)

    if decoupling:
        neg_mask = 1 - torch.eye(current_batch_len, device=hazard_pred.device)
        R_mat = R_mat * neg_mask

    negative_loss = torch.logsumexp(theta.view(1, -1).expand(current_batch_len, -1) * R_mat + SMALL_NUM * (1 - R_mat),
                                    dim=1)
    loss_cox = theta - negative_loss
    loss_cox = loss_cox[(censor != 1)]
    return -torch.mean(loss_cox)


def hazard_average_by_patient(hazard: np.ndarray, censor: np.ndarray, surv_time: np.ndarray,
                              study_id: np.ndarray, drop_partial=False):
    df = pd.DataFrame(
        {'hazard': hazard, 'censor': censor, 'surv_time': surv_time, 'study_id': study_id})

    aggregation = df.groupby('study_id').agg('max')
    aggregation['study_id'] = aggregation.index  # to get the study id as well
    return aggregation.hazard.values, aggregation.censor.values, aggregation.partial.values, aggregation.surv_time.values, aggregation


def c_index_score(hazards, censored, survtime_all):
    return lifelines.utils.concordance_index(survtime_all, -hazards, 1 - censored)


